    // 获取当前目录下所有的pdf文件
    public static List<String> GetPdfFileName(String fileAbsolutePath) {
        String[] showName;
        List<String> pdfNameList = new ArrayList<>();
        File file = new File(fileAbsolutePath);
        if (!file.exists()) {// 如果文件夹不存在则创建
            file.mkdirs();
        }
        File[] subFile = file.listFiles();

        for (int iFileLength = 0; iFileLength < subFile.length; iFileLength++) {
            // 判断是否为文件夹
            if (!subFile[iFileLength].isDirectory()) {
                String filename = subFile[iFileLength].getName();
                // 判断是否为pdf结尾
                if (filename.trim().toLowerCase().endsWith(".pdf")) {
                    showName =  filename.split(".pdf");
                    pdfNameList.add(showName[0]);
                }
            }
        }
        return pdfNameList;
    }


